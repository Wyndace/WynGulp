// === SCSS Task ===================================================================================================================================================================================================================================================

// ==== Plugins ====================================================================================================================================================================================================================================================

import gulp from "gulp";
import size from "gulp-size";
import plumber from "gulp-plumber";
import notify from "gulp-notify";
import rename from "gulp-rename";
import autoprefixer from "gulp-autoprefixer";
import groupCssMediaQueries from "gulp-group-css-media-queries";
import gulpSass from 'gulp-sass';
import csso from "gulp-csso";
import webpCss from "gulp-webp-css-fixed";

import dartSass from 'sass';

// =================================================================================================================================================================================================================================================================


// ==== Configs ====================================================================================================================================================================================================================================================

import path from "../config/path.js"
import settings from "../config/settings.js"

// =================================================================================================================================================================================================================================================================

// ==== SCSS processing ============================================================================================================================================================================================================================================

const sass = gulpSass(dartSass);

const scssTask = () => {
    return gulp.src(path.scss.src, {sourcemaps: settings.isDev})
        .pipe(plumber({
            errorHandler: notify.onError(error => ({
                title: "SCSS TASK",
                message: error
            }))
        }))
        .pipe(sass())
        .pipe(webpCss())
        .pipe(groupCssMediaQueries())
        .pipe(autoprefixer(settings.autoprefixer))
        .pipe(size({
            title: '[SCSS TASK] Before minify'
        }))
        .pipe(csso())
        .pipe(rename({ suffix: ".min" }))
        .pipe(size({
            title: '[SCSS TASK] After minify'
        }))
        .pipe(gulp.dest(path.scss.dest, {sourcemaps: settings.isDev}))
};

// =================================================================================================================================================================================================================================================================

// ==== Exporting Task =============================================================================================================================================================================================================================================

export default scssTask;

